package org.cso.MobileSpotBilling;

public class SpinnerData {
	String spinnerText;
	String value;
	
	public SpinnerData(String spinnerText){
		this.spinnerText=spinnerText;
		this.value=value;
	}
	
	public String getSpinnertext(){
		return spinnerText;
	}
	public String getValue(){
		return value;
	}
	public String toString(){
		return spinnerText;
	}

}
